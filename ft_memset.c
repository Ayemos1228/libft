#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = (char *)b;
	while (i++ < len)
		*(ptr++) = (unsigned char)c;
	return (b);
}
